package com.sp.vo;

public class EmployerVO {

	private String sno;
	private String sname;
	private String jan_sal;
	private String feb_sal;
	private String march_sal;
	
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getJan_sal() {
		return jan_sal;
	}
	public void setJan_sal(String jan_sal) {
		this.jan_sal = jan_sal;
	}
	public String getFeb_sal() {
		return feb_sal;
	}
	public void setFeb_sal(String feb_sal) {
		this.feb_sal = feb_sal;
	}
	public String getMarch_sal() {
		return march_sal;
	}
	public void setMarch_sal(String march_sal) {
		this.march_sal = march_sal;
	}
	
	
}
