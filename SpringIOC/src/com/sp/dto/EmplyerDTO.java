package com.sp.dto;

public class EmplyerDTO {

	private String sno;
	private String sname;
	private float jan_sal;
	private float feb_sal;
	private float march_sal;
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public float getJan_sal() {
		return jan_sal;
	}
	public void setJan_sal(float jan_sal) {
		this.jan_sal = jan_sal;
	}
	public float getFeb_sal() {
		return feb_sal;
	}
	public void setFeb_sal(float feb_sal) {
		this.feb_sal = feb_sal;
	}
	public float getMarch_sal() {
		return march_sal;
	}
	public void setMarch_sal(float march_sal) {
		this.march_sal = march_sal;
	}
	
	
}
